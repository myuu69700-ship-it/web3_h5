// 组合式函数
export function useI18n() {
  const languageStore = useLanguageStore()
  
  // 响应式翻译函数
  // 直接访问 store 的 getter，Vue 会自动追踪响应式
  const t = (key) => {
    const lang = languageStore.getCurrentLang
    return messages[lang]?.[key] || messages['zh-TW'][key] || key
  }
  
  return {
    t,
    currentLang: computed(() => languageStore.getCurrentLang),
    setLanguage: (lang) => languageStore.setLanguage(lang)
  }
}

